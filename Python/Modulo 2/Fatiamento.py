tupla = ("p", "y", "t", "h", "o", "n")
tupla[2:] # ('t', 'h', 'o', 'n')
tupla[:2] # ('p', 'y')
tupla[1:3] # ('y', 't')
tupla[0:3:2] # ('p', 't')
tupla[::]   # ('p', 'y', 't', 'h', 'o', 'n')
tupla[::-1] # ('n', 'o', 'h', 't', 'y', 'p')

# O fatiamento é uma forma de acessar partes de uma sequência, como uma lista ou uma tupla.
# O fatiamento é feito com o uso de colchetes e dois pontos.
# tupla[2:] retorna todos os elementos a partir do índice 2.
# tupla[:2] retorna todos os elementos até o índice 2.
# tupla[1:3] retorna os elementos entre os índices 1 e 3.
# tupla[0:3:2] retorna os elementos entre os índices 0 e 3, com um passo de 2.
# tupla[::] retorna todos os elementos da sequência.
# tupla[::-1] retorna todos os elementos da sequência em ordem reversa.
# O fatiamento pode ser usado em listas, tuplas e strings.

